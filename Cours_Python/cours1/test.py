# Print
print("Bonjour")

# Affectation
a = 2  # int
b = 2.4  # float
c = "j"  # string
d = True  # boolean
e = 1 + 2j  # complex
f = (2, 5)  # tuple
g = {2: 5}  # dictionary
h = [1]  # list

# Le type
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
print(type(g))
print(type(h))

# La concatenation
print("La valeur de a:", a)
print("La valeur de b:", b)
print("La valeur de c:", c)
print("La valeur de d:", d)
print("La valeur de e:", e)
print("La valeur de f:", f)
print("La valeur de g:", g)
print("La valeur de h:", h)

# Les operateurs arithmetiques (+, -, *, /, %, **, //)
a = 30
b = 4
print(a % b)
print(a**b)
print(a // b)

# Operateurs de comparaison
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b)
print(a != b)
print(a is b)
print(a is not b)

# Operateurs logiques
a = True
b = False
print(a and b)
print(a or b)
print(not (a))

# Complexe
a = 3j + 1
b = 5j + 3
c = complex(3, 5)
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a.real)
print(a.imag)
print(c)
